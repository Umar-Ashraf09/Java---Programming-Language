
public class IfStatements4 {
    public static void main(String[] args) {
        int amount = 20;
        if(amount < 20) {
            System.out.println("It's True");
        }
        else if(amount > 30) {
            System.out.println("It's False");
        }
        else {
            System.out.println("None of the above");
        }
    }
}